/**
 * Translate an English Quran word translation to German.
 * Returns the German translation if found, otherwise the original English.
 */
export function translateToGerman(english: string): string {
  if (!english) return "";

  // Direct lookup
  const direct = enToDeDict[english];
  if (direct) return direct;

  // Try case-insensitive lookup
  const lowerKey = Object.keys(enToDeDict).find(
    (k) => k.toLowerCase() === english.toLowerCase()
  );
  if (lowerKey) return enToDeDict[lowerKey];

  return english;
}

/**
 * Check if a German translation exists for the given English text.
 */
export function hasGermanTranslation(english: string): boolean {
  if (!english) return false;
  if (enToDeDict[english]) return true;
  return Object.keys(enToDeDict).some(
    (k) => k.toLowerCase() === english.toLowerCase()
  );
}
